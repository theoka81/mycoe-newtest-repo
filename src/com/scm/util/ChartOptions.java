package com.scm.util;

public class ChartOptions {
	
	private String comparableKey;
	private String comparableRowKey;
	private String comparableColumnKey;
	private double value;
	public String getComparableKey() {
		return comparableKey;
	}
	public void setComparableKey(String comparableKey) {
		this.comparableKey = comparableKey;
	}
	public String getComparableRowKey() {
		return comparableRowKey;
	}
	public void setComparableRowKey(String comparableRowKey) {
		this.comparableRowKey = comparableRowKey;
	}
	public String getComparableColumnKey() {
		return comparableColumnKey;
	}
	public void setComparableColumnKey(String comparableColumnKey) {
		this.comparableColumnKey = comparableColumnKey;
	}
	public double getValue() {
		return value;
	}
	public void setValue(double value) {
		this.value = value;
	}
	
	
	

}
