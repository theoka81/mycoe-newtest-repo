package com.scm.modal;

public class DashBoardViewModal {

	private String type;
	private Integer complaints;
	private Integer enquiries;

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public Integer getComplaints() {
		return complaints;
	}

	public void setComplaints(Integer complaints) {
		this.complaints = complaints;
	}

	public Integer getEnquiries() {
		return enquiries;
	}

	public void setEnquiries(Integer enquiries) {
		this.enquiries = enquiries;
	}

}
